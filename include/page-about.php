    <section class="about__section">
      <h1><span>事業理念</span></h1>
      <p class="about__slogan">人に優しく、<br>自分に厳しく、<br>助け合い、<br>楽しくする</p>
      <p>人として。<br>責任力と誠実さを尊重します。<br>事業として利益を求めることはもちろん重要ですが、その前に人として人と向き合うことを重視します。</p>
      
      <h2><span>人に優しく</span></h2>
      <p>「無理のない範囲」での他人への気配りを大切にします。そして「無理のない範囲」を「広げる努力」は自己成長において欠かせない要素と考えます。</p>

      <h2><span>自分に厳しく</span></h2>
      <p>厳しさにはいくつか種類があります。</p>
      <ul>
        <li>課題に対して積極的に取り組む姿勢</li>
        <li>何かに失敗した際は、その過ちを認め、問題を解決するか開示する姿勢</li>
        <li>取捨選択を誠実におこなう姿勢</li>
      </ul>
      <p>これらの姿勢を実現し続けることで自己成長につなげます。</p>
      <p>取捨選択の補足：仮に究極の2つの選択があるとします。どちらかを選べば選ばなかったほうが不幸になり、どちらも選択しなければ自分が不幸になる場合、このような光と影の関係を理解し、誠実に選択できる強さを持てるよう努力します。</p>

      <h2><span>助け合い</span></h2>
      <p>数学では「負」と「負」を掛け合わせると「正」になりますが、シンプルシンプルデザインではこの考えは当てはまりません。どちらか(もしくは双方)が負担となる助け合いではなく、前向きな関係での助け合いを行います。「困っている人を助けるな」「見返りを求めろ」と言っているのでは決してありません。その結果をイメージするようにします。助け合う基準、それは「お互いがハッピーになりそうかどうか」です。</p>
      <p>一方、助けを求める勇気も必要だと考えます。責任感が強いあまり自身で抱え込んでしまってはいけません。必要であるのは分かっているけどそれが不得意で悩むくらいなら、それが得意な人に助けを求めます。人には得意・不得意があります。成長は必要ですが、完璧や万能である必要はありません。<br>人に優しく、自分に厳しく、助け合います。</p>

      <h2><span>楽しくする</span></h2>
      <p>自他ともに「楽しい！」と感じられるサービスや製品を創り続けます。「楽しんでる？」と聞かれたら「うん」と言える、「楽しんでる？」と聞いたら「まあね」と答えてくれる、そんな関係を築き上げられるような事業展開を目指します。</p>
      <p>その結果、誰かの役に立ち、社会に貢献できることを強く望みます。<br>そして、利益は後から付いてくるものと信じています。</p>
      <p>シンプルシンプルデザインでは、この事業理念を遵守し、事業理念に合致する企画・制作・運用を、必要に応じてご提案・実施してまいります。</p>
    </section>
    
